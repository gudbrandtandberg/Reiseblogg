<h3>The Mysteries of Bali</h3>

<p>
Although Bali to many people is a place of party, beachlife and Bintang beer it is also home to the most complex and interesting religion and culture I have ever gotten to know. In this article I will try to explain some parts of their extensive culture, rituals and beliefs. It will in no way cover all there is to say about the subject but merely the ones we encountered on our one-month travel through this charming little island.
</p>
 

<h4>Names</h4>

<p>
Brothers and sisters don't necessaraly have the same last name. This is because there are no actual family names here. The parents decide on the individual last names at least three months after the birth of the child. That gives them enough time to get to know the child and give an appropriate name. Some names are given according to the looks of the child like Apel (apple) if the child is fat. Others get names based on their whims or the goodness of their heart.
</p>
 
<p>
As you see the parents have a lot of freedom when it comes to choosing a last name, but they have very little choice when it comes to first names. First names are given according to rank of birth. The first child is named Wayan or Putu. Gede is also possible but only for boys. The second child is named Made or Kadek, the third Nyoman or Komang and the fourth Ketut. What if there is a fifth child? Well, then they start all over again.
</p>
 
<p>
We learned this fact last time we where in Bali, three years ago, but traveling around so much this time we where taken aback by the amount of Ketuts we met and by all the "Made's Warung" (Made's restaurant) all over Bali. You would think this made it easier to remember names, but I found it rather difficult.
</p>
 

<h4>
    Balinese Hinduism
