<p>
We came to Ubud on the first of three days of the September Celebrations. All over town men and women dressed up in traditional clothing getting ready to visit the temple. The women carried pyramid-shaped offerings on their heads consisting of fruits, cakes, eggs and sometimes entire chickens and walked to the temple to have their offerings blessed. We weren't let into the temple but we stood outside of one of the larger ones stunned by the show of people and a traditional music orchestra (press play to listen!).
</p>

