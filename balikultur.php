<p>
The offerings are placed on the floor because it is the most impure part of the earth, but the demons wander just about everywhere. The demons are only capable of moving in a straight line and this is why you will often meet a wall entering through a door in a balinese home and either go to the left or right to enter the room. Apparently they like hanging out at crossroads and cause a lot of accidents and when this happens a priest quickly shows up and performs a ceremony. They also put roosters on the side of the road inside large bamboo-woven basket cages. I haven't completely understood the direct effect of this act, but it either makes the demons happy or keeps them out of the house.
</p>

