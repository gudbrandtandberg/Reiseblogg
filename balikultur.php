</h4>

 
<p>
According to Balinese beliefs, the Gods left them the earth, but in exchange, they count on them to maintain the balance of it.
</p>
 
<p>
The earth is seen as a big ball resting on a huge turtle called Bedawang. Around its feet are coiled two large guardian snakes - the Nagas. Way above the earth, at the top of the Agung volcano, the Gods and the ancestors dwell. Human beings only occupy the intermediate part, that they lent us, where demons are free to come and cause damage. Sometimes, when everything is going wrong, the turtle wakes up and starts moving, which causes earthquakes. To make them cease as quickly as possible, the balinese must make a lot of noise in order to wake up the two guardian snakes.
</p>
 
<p>
But that is not all! In order to fullfill their mission they must endeavour to not only honour the Gods, but also pay attention to appease the demons that are prowling about. They are both very numerous, so that means a lot of work!
</p>
 
<p>
In Bali, there is a God for nearly everything and every God has its own day when they are celebrated. There are too many of them for me to mention them all but take Brahma for example. He  is the God of moving metal objects, like motorbikes and cars. So whenever they buy a new vehicle there is a ceremony in its honour so that the vehicle will be protected.
</p>

