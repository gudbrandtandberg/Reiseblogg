<p>
As  I mentiond, pleasing the Gods isn't the only concern of the Balinese hindus, and the demons must also be paid sufficient attention to. That is why they every day, at home and work, after placing offerings to the Gods in raised shrines, place offerings on the ground. These are intended for the demons. Each basket must contain flower petals and sweet foods, be sprinkled with holy water and topped with an incense stick and sometimes a little something they think the demons will enjoy.
</p>
 
