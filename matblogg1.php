    <p>
    These first pictures are from Thailand. I'll get back to thaifood in a later post, but for now let's focus on Balinese food! Balinese food is very unique and very tasty. Not as spicy as other southeastern asian cuisines (which suits me well), but still very featured and flavoursome. Many dishes start of with a spice mix <i>basa gede</i>, typically some combination of garlic, chili, tumeric, shallots, nutmeg, ginger, palm sugar, cumin and local mystery spices. The spice rub or chili paste is either stir-fried with vegetables - local cabbages, carrots, long beans, etc. - and <i>kecap manis</i>, or used to marinate pork or chicken. Thin slices of meat are skewered and grilled on coal (<i>sate</i>) and smothered in sauce. In the end always served with rice steamed in banana leaves.
    </p>
    <p>
    Peanuts! I'm very happy to be on an island where everyone loves peanuts as much as I. The traditional sweet soy-spice-peanut sauce is ubiquitous, notably used for dipping <i>sate</i> and <i>Gado Gado</i> - one of my favorites, consisting of steamed vegetables served with rice, peanut sauce and crackers.
    </p>
    <p>
    For most meals, we head over to the local food market (every town has one), where 5-10 different mobile kitchens or <i>warung</i>'s set up and serve their own speciality. For breakfast I've fallen completely for a sweet green bean porrige <i>Bubur Kecang Ijo</i>, made with boiled mung beans, black glutinous rice, palm sugar, hint of ginger and topped with fresh coconut milk. Among the dinner-winners, I must mention <i>Martabak</i> &mdash; super-thin shallow fried pancakes filled with eggs and green onions, served with chili and pickled cucmber; scrumptious! Deep frying is unsurprisingly very common &mdash; chicken bits, vegetables, tofu, tempeh &mdash; all good, but nothing very special. Fried bananas on the other hand, my goodness! The bananas are smaller and meatier, sweeter, somehow more like sweet potatoes but softer. Deep fried and served with <i>Kopi Bali</i> (sweet, strong coffee with condensed milk) it makes me very happy:
